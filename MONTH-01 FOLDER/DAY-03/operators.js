let value1 = 20;
let value2 = 6;

// Basic Arithmetic Operations
let addition = value1 + value2;       // 26
let subtraction = value1 - value2;    // 14
let multiplication = value1 * value2; // 120
let division = value1 / value2;       // 3.333...
let remainder = value1 % value2;      // Modulus: 2 (20 divided by 6 leaves a remainder of 2)

console.log("JS Arithmetic Results:");
console.log("Add:", addition);
console.log("Subtract:", subtraction);
console.log("Multiply:", multiplication);
console.log("Divide:", division);
console.log("Remainder:", remainder);